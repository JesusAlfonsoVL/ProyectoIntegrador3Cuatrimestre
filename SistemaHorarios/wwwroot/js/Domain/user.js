class User {
    Id= 0;
    Name;
    Address;
    Phone;
    Email;
    UserName;
    Password;
    constructor(Id, Name, Address, Phone, Email, UserName, Password){
    this.Id = Id;
    this.Name = Name;
    this.Address = Address;
    this.Phone = Phone;
    this.Email = Email;
    this.UserName =UserName;
    this.Password = Password;
    }
}